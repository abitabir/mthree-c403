public class Calc {
    
    public int add(int num1, int num2) {
        return num1 + num2;
    }
    
    public int sub(int num1, int num2) {
        return num1 - num2;
    }
    
    public int mult(int num1, int num2) {
        return num1 * num2;
    }
    
    public float div(int num1, int num2) {
        return (float) num1 / (float) num2;
    }
    
    public int divFloor(int num1, int num2) {
        return num1 / num2;
    }
    
}
